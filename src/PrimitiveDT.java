public class PrimitiveDT {
    public static void main(String[] args) {
        System.out.println("Byte Default Value is: 0");
        System.out.println("Short Default Value is: 0");
        System.out.println("Int Default value is: 0");
        System.out.println("Long default value is: 0");
        System.out.println("Float default value is: 0.0f");
        System.out.println("Double Default value is: 0.0d");
        System.out.println("Boolean Default Value is: False");
        System.out.println("Char Default Value is: '\u0000'");
    }
}
